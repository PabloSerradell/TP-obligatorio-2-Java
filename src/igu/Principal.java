    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        String numero_cliente = txtClienteN.getText();
        String nombre_perro = txtNombrePerro.getText();
        String raza = txtRaza.getText();
        String color = txtColor.getText();
        
        boolean alergico;
        String aux1 = (String) cmbAlergico.getSelectedItem();
        if (aux1.equals("Si")){
            alergico = true;
        }else{
            alergico = false;
        }
        
        boolean atencion_especial;
        String aux2 = (String) cmbAtencionEspecial.getSelectedItem();
        if (aux2.equals("Si")){
            atencion_especial = true;
        }else{
            atencion_especial = false;
        }
        
        String nombre_duenio = txtNombreDuenio.getText();
        String celular_duenio = txtCelularDuenio.getText();
        String observaciones = txtObservaciones.getText();
        
        control.crearPerro(numero_cliente, nombre_perro, raza, color, alergico, atencion_especial, nombre_duenio, celular_duenio, observaciones);
        
        JOptionPane.showMessageDialog(rootPane, "Perro Agregado correctamente", "Perro agregado", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnGuardarActionPerformed

    

